import MOD
import MDM, MDM2
import sys
import RX_API

def init():
    sendAT('ATE0', 'OK')
    sendAT('ATS0=0', 'OK')
    sendAT('AT+CRC=1', 'OK', 2, 2)
    sendAT('AT+CLIP=1', 'OK', 2, 2)

def sendAT(request, response, timeout = 2, interface = 1):
    if(interface == 1):
        MDM.send(request + '\r', 2)
    else:
        MDM2.send(request + '\r', 2)
    result = -2
    data = ''
    timer = MOD.secCounter() + timeout
    while(MOD.secCounter() < timer):
        if(interface == 1):
            rcv = MDM.read()
        else:
            rcv = MDM2.read()
        if(len(rcv) > 0):
            data = data + rcv
            if(data.find(response) != -1):
                result = 0
                break
            if(data.find('ERROR') != -1):
                result = -1
                break
    return (result, data)

def checkRing():
    data = MDM2.receive(5)
    pos = data.find('+CLIP')
    if(pos != -1):
        data = data[pos + 6:].strip()
        tokens = data.split(',')
        if(len(tokens) > 0):
            number = tokens[0].replace('"', '')
            return number
    return ''

def waitRegister():
    while(1):
        RX_API.resetWDT()
        r, d = sendAT('AT+CREG?', '+CREG: 0,1')
        if(r == 0):
            break
        MOD.sleep(20)
  
def hangUp():
    sendAT('ATH', 'OK')

def reboot():
    sendAT('AT#ENHRST=1,0', 'OK')
    MOD.sleep(2)
    sys.exit()

